package net.md_5.bungee.api.config;

/**
 * Represents a player's skin settings.
 */
public interface SkinConfiguration
{

    boolean hasCape();

    boolean hasJacket();

    boolean hasLeftSleeve();

    boolean hasRightSleeve();

    boolean hasLeftPants();

    boolean hasRightPants();

    boolean hasHat();

}